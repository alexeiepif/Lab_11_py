import math

def cylinder():
    """
    Вычисляет площадь цилиндра в зависимости от выбора пользователя.
    
    Сначала функция спрашивает у пользователя его предпочтение относительно
    расчета площади: только боковая поверхность или полная площадь цилиндра.
    Затем производит соответствующие вычисления и выводит результат.
    """
    
    def circle(radius):
        """
        Вычисляет площадь круга по радиусу.
        
        Принимает радиус круга и возвращает площадь, используя формулу
        площади круга: pi * r^2.
        """
        return math.pi * radius ** 2
    
    radius = float(input("Введите радиус цилиндра: "))
    height = float(input("Введите высоту цилиндра: "))
    
    base_area = circle(radius)
    side_area = 2 * math.pi * radius * height
    full_area = side_area + 2 * base_area
    
    user_choice = input("Введите 'боковая' для площади боковой поверхности или 'полная' для полной площади цилиндра: ")
    
    if user_choice.lower() == 'боковая':
        print(f"Площадь боковой поверхности цилиндра: {side_area:.2f}")
    elif user_choice.lower() == 'полная':
        print(f"Полная площадь цилиндра: {full_area:.2f}")
    else:
        print("Неправильный ввод. Пожалуйста, введите 'боковая' или 'полная'.")

# Основная ветка программы
if __name__ == '__main__':
    cylinder()
